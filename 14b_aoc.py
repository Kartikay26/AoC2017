def filled_points():
	r = []
	for (i,l) in z(grid):
		for (j,x) in z(l):
			if x=='#':
				r.append((i,j))
	return r

def create_regions():
	for pt in filled_points():
		regions[pt] = {pt}

def check_adj(pt):
	i,j = pt
	adj = [(1,0),(-1,0),(0,1),(0,-1)]
	for (x,y) in adj:
		if verify_coords(i+x,j+y):
			if grid[i+x][j+y] == '#':
				pt2 = (i+x,j+y)
				unify(pt,pt2)

def get_region(ptx):
	for pt in regions:
		if ptx in regions[pt]:
			return pt

def unify(pt1, pt2):
	check()
	if (pt1 in regions) and (pt2 in regions):
		set_a = regions[pt1]
		set_b = regions[pt2]
		set_u = set_a.union(set_b)
		regions[pt1] = set_u
		regions.pop(pt2)
	elif pt1 in regions:
		pt3 = get_region(pt2)
		set_a = regions[pt1]
		set_b = regions[pt3]
		set_u = set_a.union(set_b)
		regions[pt1] = set_u
	elif pt2 in regions:
		pt3 = get_region(pt1)
		set_a = regions[pt3]
		set_b = regions[pt2]
		set_u = set_a.union(set_b)
		regions[pt2] = set_u
	else:
		# find where are pt1 and pt2 and combine them
		pt3 = get_region(pt1)
		pt4 = get_region(pt2)
		if pt3 == pt4:
			pass
		set_a = regions[pt3]
		set_b = regions[pt4]
		set_u = set_a.union(set_b)
		regions[pt3] = set_u
		regions.pop(pt4)

